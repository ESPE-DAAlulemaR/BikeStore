    public void refreshTable()
    {
        DefaultTableModel model = (DefaultTableModel) this.tblUsers.getModel();

        model.setRowCount(0);

        ArrayList<User> users = UserController.index();
        for (User user : users) {
            Object[] row = { user.user_id, user.name, user.lastname, user.username, user.email, user.password };
            model.addRow(row);
        }
    }

